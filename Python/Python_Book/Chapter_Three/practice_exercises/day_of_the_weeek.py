# Day of the week

# Taking user input
number = int(input("Enter a number from 1-7:"))

# Conditions from user input
if number == 1:
    print("Monday")
elif number == 2:
    print("Tuesday")
elif number == 3:
    print("Wednesday")
elif number == 4:
    print("Thursday")
elif number == 5:
    print("Friday")
elif number == 6:
    print("Saturday")
elif number == 7:
    print("Sunday")
else:
    print("Not a vaild number for the day of the week")