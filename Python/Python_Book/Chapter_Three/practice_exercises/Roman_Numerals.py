# Roman numerals from 1-10

# Taking a user input
number = int(input("Enter a number between 1-10: "))

# Checking condition for user input from 1-10
if number == 1:
    print("I")
elif number == 2:
    print("II")
elif number == 3:
    print("III")
elif number == 4:
    print("IV")
elif number == 5:
    print("V")
elif number == 6:
    print("VI")
elif number == 7:
    print("VII")
elif number == 8:
    print("vIII")
elif number == 9:
    print("IX")
elif number == 10:
    print("X")
else:
    print("Not a valid range of numbers")
